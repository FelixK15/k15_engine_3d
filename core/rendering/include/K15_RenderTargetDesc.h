#ifndef _K15_Rendering_RenderTargetDesc_h_
#define _K15_Rendering_RenderTargetDesc_h_

#include "K15_RenderPrerequisites.h"
#include "K15_RenderFormats.h"

enum K15_RenderTargetDimension : uint32
{
	K15_RENDER_TARGET_DIMENSION_FULL_RESOLUTION = 0,
	K15_RENDER_TARGET_DIMENSION_HALF_RESOLUTION,
	K15_RENDER_TARGET_DIMENSION_QUARTER_RESOLUTION
};

enum K15_RenderTargetOutputBitmask : uint32
{
	K15_RENDER_TARGET_OUTPUT_COLOR_0_BIT = 0x000001,
	K15_RENDER_TARGET_OUTPUT_COLOR_1_BIT = 0x000002,
	K15_RENDER_TARGET_OUTPUT_COLOR_2_BIT = 0x000004,
	K15_RENDER_TARGET_OUTPUT_COLOR_3_BIT = 0x000008,
	K15_RENDER_TARGET_OUTPUT_COLOR_4_BIT = 0x000010,
	K15_RENDER_TARGET_OUTPUT_COLOR_5_BIT = 0x000020,
	K15_RENDER_TARGET_OUTPUT_COLOR_6_BIT = 0x000040,
	K15_RENDER_TARGET_OUTPUT_COLOR_7_BIT = 0x000080,

	K15_RENDER_TARGET_OUTPUT_DEPTH_BIT	  = 0x000100,
	K15_RENDER_TARGET_OUTPUT_STENCIL_BIT  = 0x000200
};

struct K15_RenderTargetDesc
{
	K15_RenderTargetDimension dimension;
	K15_RenderFormat format;

	uint32 outputFlags;
};

#endif //_K15_Rendering_RenderTargetDesc_h_